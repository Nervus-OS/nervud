// Package sysprobe 集中纯观察性系统调用：向 Linux 内核询问事实但不改变任何系统状态
//
// 为什么不放进 authority
//
// Authority Gate 的全部价值在于特权操作可枚举。把 SO_PEERCRED 这类
// 任何普通进程都能对自己的 socket 调用的操作塞进 Gate，导致审计日志
// 被无特权噪音淹没，Kind 枚举里混进不需要裁决的条目
//
// 更危险的是反过来做：在 authority 包内开一个不走 do 的可导出直通口
// authority.go 已经写明run 是包内不可导出的平台实现，不接受外部注入
// 否则 Gate 就退化成帮你审计一下你自己写的任意操作；一个同包的、
// 不审计的 syscall 直通函数，是从另一侧开了同一扇门，而且它没有任何结构性
// 约束（没有 Kind、没有 Validate、没有审计），下一个人往里加什么全凭自觉
//
// 因此本包独立成包
//
// 准入标准
//
//  1. 纯观察 - 调用成功或失败，系统状态都不改变；调用者只是得到了信息
//     这是主判据，另外三条是补强
//  2. 自作用域 - 只作用于调用者已经持有的对象（自己的 fd、自己的线程）
//     不作用于全局系统状态，也不接受随便给个对象都行的句柄
//  3. 零 capability - 普通用户进程调用同样成功；需要 capability 说明它是
//     一次权力行使，应当去 Gate 或去拥有该机制的模块
//  4. 类型化窄签名 - 禁止 Getsockopt(fd, level, opt int) 这类通用直通。
//     通用直通等于把整个 syscall 表间接导出，depguard 就白设了
//
// 本包无状态、无构造函数、无生命周期：直接 import 调用即可，不在 main.go 装配，
// 也不通过窄接口注入（没有需要替换的实现，注入只会凭空多一层）
package sysprobe
